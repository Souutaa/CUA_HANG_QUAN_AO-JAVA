    private void btnTHEMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTHEMActionPerformed
        NHACUNGCAP ncc = new NHACUNGCAP();
        int ktra = 1;
        NhaCungCapBUS nccBUS = new NhaCungCapBUS();
        ncc.setMaNCC(jtfMA_NCC.getText());
        if (jtfMA_NCC.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Lỗi không được để Mã NCC trống", "Lôi", JOptionPane.ERROR_MESSAGE);
        } else {
            for (NHACUNGCAP mancc : NhaCungCapBUS.NHACUNGCAPLIST) {
                if (ncc.getMaNCC().equalsIgnoreCase(mancc.getMaNCC())) {
                    ktra = 0;
                }
            }
            if (ktra == 1) {

                ncc.setTenNCC(jtfTEN_NCC.getText());
                ncc.setSDT_NCC(jtfSDT_NCC.getText());
                ncc.setDiaChi_NCC(jtfDIACHI_NCC.getText());

                //NhanVienDAO.insert(nv);
                try {
                    int result = nccBUS.insertUser(ncc);
                    if (result != 0) {
                        JOptionPane.showMessageDialog(this,
                                "Thêm dữ liệu thành công!", "Thông báo",
                                JOptionPane.INFORMATION_MESSAGE);
                    }
                    showNHACUNGCAP();
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Mã NCC đã bị trùng lặp do khởi tạo trước đó", "Lôi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnTHEMActionPerformed

    private void btnXOAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXOAActionPerformed
        NhaCungCapBUS nccBUS = new NhaCungCapBUS();
        int selectedIndex = jtbNHACUNGCAP.getSelectedRow();
        if (selectedIndex >= 0) {
            NHACUNGCAP std = NhaCungCapBUS.NHACUNGCAPLIST.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this item?");

            if (option == 0) {
                nccBUS.deleteUser(std.getMaNCC());

                showNHACUNGCAP();
            }
        }
    }//GEN-LAST:event_btnXOAActionPerformed

    private void btnSUAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSUAActionPerformed
        int i = jtbNHACUNGCAP.getSelectedRow();
        if (i >= 0) {
            NHACUNGCAP nccold = new NHACUNGCAP();
            NHACUNGCAP ncc = new NHACUNGCAP();
            NhaCungCapBUS nccBUS = new NhaCungCapBUS();
            nccold = NhaCungCapBUS.NHACUNGCAPLIST.set(i, ncc);

            ncc.setMaNCC(jtfMA_NCC.getText());
            ncc.setTenNCC(jtfTEN_NCC.getText());
            ncc.setSDT_NCC(jtfSDT_NCC.getText());
            ncc.setDiaChi_NCC(jtfDIACHI_NCC.getText());

            int result = nccBUS.updateUser(ncc, nccold);
            if (result == 1) {
                JOptionPane.showMessageDialog(this,
                        "Cập nhật dữ liệu thành công!", "Thông báo",
                        JOptionPane.INFORMATION_MESSAGE);
                showNHACUNGCAP();

            } else {
                JOptionPane.showMessageDialog(this, "Lỗi");
            }
        }
    }//GEN-LAST:event_btnSUAActionPerformed

