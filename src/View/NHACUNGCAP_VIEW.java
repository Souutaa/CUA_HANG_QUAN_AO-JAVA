    private void jtbNHACUNGCAPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbNHACUNGCAPMouseClicked
        if (evt.getSource() == jtbNHACUNGCAP) {
            int i = jtbNHACUNGCAP.getSelectedRow();
            if (i >= 0) {
                NHACUNGCAP ncc = new NHACUNGCAP();
                ncc = NhaCungCapBUS.NHACUNGCAPLIST.get(i);
                jtfMA_NCC.setText(ncc.getMaNCC());
                jtfTEN_NCC.setText(ncc.getTenNCC());
                jtfSDT_NCC.setText(ncc.getSDT_NCC());
                jtfDIACHI_NCC.setText(ncc.getDiaChi_NCC());
            }
        }
    }//GEN-LAST:event_jtbNHACUNGCAPMouseClicked


