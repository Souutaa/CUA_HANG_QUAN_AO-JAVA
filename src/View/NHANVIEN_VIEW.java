    public void showNHANVIEN() {

        NhanVienBUS nvBUS = new NhanVienBUS();
        NhanVienBUS.NHANVIENLIST = nvBUS.getAllUsers();

        tableModel.setRowCount(0);
        // Kiểu dữ liệu : biến : Phạm vi truy cập 
        for (NHANVIEN nv : NhanVienBUS.NHANVIENLIST) {
            Vector<String> row = new Vector<>();
            row.add(nv.getMaNV());
            row.add(nv.getTenNV());
            row.add(nv.getSDT_NV());
            row.add(nv.getNgaySinh_NV());
            row.add(nv.getDiaChi_NV());
            tableModel.addRow(row);
        }

        jtbNHANVIEN.setModel(tableModel);
    }

