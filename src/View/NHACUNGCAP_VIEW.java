    private void showNHACUNGCAP() {

        NhaCungCapBUS nccBUS = new NhaCungCapBUS();
        NhaCungCapBUS.NHACUNGCAPLIST = nccBUS.getAllUsers();

        tableModel.setRowCount(0);

        for (NHACUNGCAP ncc : NhaCungCapBUS.NHACUNGCAPLIST) {
            Vector<String> row = new Vector<>();
            row.add(ncc.getMaNCC());
            row.add(ncc.getTenNCC());
            row.add(ncc.getSDT_NCC());
            row.add(ncc.getDiaChi_NCC());
            tableModel.addRow(row);
        }

        jtbNHACUNGCAP.setModel(tableModel);
    }

