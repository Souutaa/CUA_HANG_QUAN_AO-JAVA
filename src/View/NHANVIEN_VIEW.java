    private void btnSUAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSUAActionPerformed
        int i = jtbNHANVIEN.getSelectedRow();
        if (i >= 0) {
            NHANVIEN nvold = new NHANVIEN();
            NHANVIEN nv = new NHANVIEN();
            NhanVienBUS nvBUS = new NhanVienBUS();
            nvold = NhanVienBUS.NHANVIENLIST.set(i, nv);

            nv.setMaNV(jtfMANV.getText());
            nv.setTenNV(jtfTENNV.getText());
            nv.setNgaySinh_NV(jtfNGAYSINH_NV.getText());
            nv.setSDT_NV(jtfSDT_NV.getText());
            nv.setDiaChi_NV(jtfDIACHI_NV.getText());

            int result = nvBUS.updateUser(nv, nvold);
            if (result == 1) {
                JOptionPane.showMessageDialog(this,
                        "Cập nhật dữ liệu thành công!", "Thông báo",
                        JOptionPane.INFORMATION_MESSAGE);
                showNHANVIEN();

            } else {
                JOptionPane.showMessageDialog(this, "Lỗi");
            }
        }
    }//GEN-LAST:event_btnSUAActionPerformed

